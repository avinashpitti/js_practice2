// printing numbers in reverse order
console.log('printing numbers in reverse order')
for(i=10;i>=1;i--){
    console.log(i)
}

//print even numbers
console.log('printing even numbers')
for(i=2;i<=20;i+=2){
    console.log(i);
}

for(let i=100;i<=120;i++){
    if(i%2===0){
        console.log(i);
    }
}

// print sum of first 5 numbers
console.log('sum of first 5 numbers')
let sum=0;
for (let i=1;i<=5;i++){
    sum=sum+i;
}
console.log(sum);

